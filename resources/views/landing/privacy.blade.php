        <h2>7. Защита персональных данных</h2>
        <p>7.1. Оператор принимает необходимые и достаточные организационные и технические меры для защиты персональных данных от неправомерного или случайного доступа, уничтожения, изменения, блокирования, копирования, распространения, а также от иных неправомерных действий третьих лиц.</p>

        <p>7.2. Меры по защите персональных данных включают:</p>
        <ul>
            <li>Использование защищённого соединения (SSL/TLS)</li>
            <li>Ограничение доступа к персональным данным</li>
            <li>Хранение паролей в зашифрованном виде</li>
            <li>Регулярное резервное копирование данных</li>
            <li>Мониторинг и предотвращение несанкционированного доступа</li>
            <li>Обучение сотрудников правилам работы с персональными данными</li>
        </ul>

