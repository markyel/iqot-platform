        <h1>Политика конфиденциальности</h1>
        <p class="meta">Дата вступления в силу: 24 декабря 2025 г.</p>

        <p class="intro">Настоящая Политика конфиденциальности (далее — «Политика») определяет порядок обработки и защиты персональных данных пользователей сервиса IQOT (далее — «Сервис», «Оператор»).</p>

        <p class="intro">Политика разработана в соответствии с Федеральным законом от 27.07.2006 № 152-ФЗ «О персональных данных» и иными нормативными правовыми актами Российской Федерации в области персональных данных.</p>

        <hr>

        <h2>1. Основные понятия</h2>
        <p><strong>Персональные данные</strong> — любая информация, относящаяся к прямо или косвенно определённому или определяемому физическому лицу (субъекту персональных данных).</p>
        <p><strong>Обработка персональных данных</strong> — любое действие (операция) или совокупность действий с персональными данными, включая сбор, запись, систематизацию, накопление, хранение, уточнение, извлечение, использование, передачу, обезличивание, блокирование, удаление, уничтожение.</p>
        <p><strong>Оператор</strong> — лицо, самостоятельно или совместно с другими лицами организующее и осуществляющее обработку персональных данных.</p>
        <p><strong>Пользователь</strong> — физическое лицо, использующее Сервис и предоставляющее свои персональные данные.</p>
        <p><strong>Сайт</strong> — совокупность веб-страниц, размещённых в сети Интернет по адресу iqot.ru.</p>

        <hr>

        <h2>2. Общие положения</h2>
        <p>2.1. Использование Сервиса означает безоговорочное согласие Пользователя с настоящей Политикой и указанными в ней условиями обработки персональных данных.</p>
        <p>2.2. В случае несогласия с условиями Политики Пользователь должен прекратить использование Сервиса.</p>
        <p>2.3. Настоящая Политика применяется только к Сервису IQOT. Оператор не контролирует и не несёт ответственности за сайты третьих лиц, на которые Пользователь может перейти по ссылкам, доступным на Сайте.</p>
        <p>2.4. Оператор не проверяет достоверность персональных данных, предоставляемых Пользователем, и не имеет возможности оценивать его дееспособность. Оператор исходит из того, что Пользователь предоставляет достоверные и достаточные персональные данные и поддерживает их в актуальном состоянии.</p>

        <hr>

        <h2>3. Перечень обрабатываемых персональных данных</h2>
        <p><strong>3.1. Данные, предоставляемые Пользователем:</strong></p>
        <ul>
            <li>Фамилия, имя, отчество</li>
            <li>Адрес электронной почты</li>
            <li>Номер контактного телефона</li>
            <li>Наименование организации</li>
            <li>Должность</li>
            <li>Юридический адрес организации</li>
            <li>ИНН и КПП организации</li>
        </ul>

        <p><strong>3.2. Данные, собираемые автоматически:</strong></p>
        <ul>
            <li>IP-адрес</li>
            <li>Данные файлов cookie</li>
            <li>Информация о браузере и устройстве</li>
            <li>Дата и время доступа к Сервису</li>
            <li>Адреса запрашиваемых страниц</li>
            <li>Реферер (адрес предыдущей страницы)</li>
        </ul>

        <hr>

        <h2>4. Цели обработки персональных данных</h2>
        <p>4.1. Оператор обрабатывает персональные данные в следующих целях:</p>
        <ul>
            <li><strong>Оказание услуг Сервиса:</strong> формирование и рассылка запросов коммерческих предложений поставщикам, сбор и консолидация ответов, предоставление отчётов Пользователю.</li>
            <li><strong>Связь с Пользователем:</strong> уведомление о статусе заявки, ответы на обращения, информирование об изменениях в работе Сервиса.</li>
            <li><strong>Улучшение качества Сервиса:</strong> анализ поведения пользователей, выявление и устранение технических неполадок, разработка новых функций.</li>
            <li><strong>Исполнение требований законодательства:</strong> соблюдение обязанностей, предусмотренных нормативными правовыми актами Российской Федерации.</li>
        </ul>

        <hr>

        <h2>5. Правовые основания обработки персональных данных</h2>
        <p>5.1. Оператор обрабатывает персональные данные на следующих правовых основаниях:</p>
        <ul>
            <li>Согласие субъекта персональных данных на обработку его персональных данных (п. 1 ч. 1 ст. 6 Федерального закона № 152-ФЗ)</li>
            <li>Исполнение договора, стороной которого является субъект персональных данных (п. 5 ч. 1 ст. 6 Федерального закона № 152-ФЗ)</li>
            <li>Осуществление прав и законных интересов Оператора (п. 7 ч. 1 ст. 6 Федерального закона № 152-ФЗ)</li>
        </ul>

        <hr>

        <h2>6. Порядок обработки персональных данных</h2>
        <p><strong>6.1. Сбор данных</strong> осуществляется путём заполнения Пользователем форм на Сайте, а также автоматически при использовании Сервиса.</p>
        <p><strong>6.2. Хранение данных</strong> осуществляется на серверах, расположенных на территории Российской Федерации, в течение срока, необходимого для достижения целей обработки, но не менее срока, установленного законодательством.</p>
        <p><strong>6.3. Использование данных</strong> осуществляется исключительно в целях, указанных в разделе 4 настоящей Политики.</p>

        <p><strong>6.4. Передача данных третьим лицам</strong> осуществляется только в следующих случаях:</p>
        <ul>
            <li>Поставщикам — в составе запросов коммерческих предложений (с согласия Пользователя)</li>
            <li>Государственным органам — по их законному требованию</li>
            <li>Иным лицам — только с явного согласия Пользователя</li>
        </ul>

        <p><strong>6.5. Уничтожение данных</strong> осуществляется по достижении целей обработки, по требованию субъекта персональных данных или при отзыве согласия на обработку, если иное не предусмотрено законодательством.</p>

